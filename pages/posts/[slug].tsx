import { useRouter } from "next/router";
import ErrorPage from "next/error";
import Head from "next/head";
import { GetStaticPaths, GetStaticProps } from "next";
import PostBody from "components/post-body";
import MoreStories from "components/more-stories";
import PostHeader from "components/post-header";
import SectionSeparator from "components/section-separator";
import Layout from "components/Layout/layout";
import PostTitle from "components/post-title";
import Tags from "components/tags";
import { getAllPostsWithSlug, getMenu, getPostAndMorePosts } from "lib/api";
import { CMS_NAME } from "lib/constants";
import Container from "components/Layout/container";

export default function Post({ post, posts, preview, allMenus }) {
  const router = useRouter();
  const gnbMenu = allMenus?.edges;
  const morePosts = posts?.edges;

  if (!router.isFallback && !post?.slug) {
    return <ErrorPage statusCode={404} />;
  }

  return (
    <Layout gnbMenu={gnbMenu}>
      <Container>
        {router.isFallback ? (
          <PostTitle>Loadingâ€¦</PostTitle>
        ) : (
          <>
            <article>
              <Head>
                <title>
                  {post.title} | Next.js Blog Example with {CMS_NAME}
                </title>
                <meta
                  property="og:image"
                  content={post.featuredImage?.node.sourceUrl}
                />
              </Head>
              <PostHeader
                title={post.title}
                coverImage={post.featuredImage}
                date={post.date}
                author={post.author}
                categories={post.categories}
              />
              <PostBody content={post.content} />
              <footer>
                {post.tags.edges.length > 0 && <Tags tags={post.tags} />}
              </footer>
            </article>

            <SectionSeparator />
            {morePosts.length > 0 && <MoreStories posts={morePosts} />}
          </>
        )}
      </Container>
    </Layout>
  );
}

export const getStaticProps: GetStaticProps = async ({
  params,
  preview = false,
  previewData,
}) => {
  const data = await getPostAndMorePosts(params?.slug, preview, previewData);
  const allMenus = await getMenu();

  return {
    props: {
      preview,
      post: data.post,
      posts: data.posts,
      allMenus,
    },
    revalidate: 10,
  };
};

export const getStaticPaths: GetStaticPaths = async () => {
  const allPosts = await getAllPostsWithSlug();

  return {
    paths: allPosts.edges.map(({ node }) => `/posts/${node.slug}`) || [],
    fallback: true,
  };
};
